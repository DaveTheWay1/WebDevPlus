// You may choose to use the do...while statement 
// instead of while to force the code block 
// to always execute at least once.
let num = 0;
do {
    console.log(num + ' is even');
    num += 2;
} while (num <= 10);
// The do-while loop is a type of loop in programming 
// that executes a block of code once before checking 
// if the condition is true, and then it will repeat 
// the loop as long as the condition remains true.

// * so the do always happens for every loop in the while as long as it is true.
