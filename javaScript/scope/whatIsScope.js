// * What is Scope?

// * In general, the concept of scope in computer programming 
// pertains to the accessibility of variables and functions from 
// a given point of the code.
// * In other words, as you write a line of code, 
// what variables and functions do you have access to?

// ? If a line of code doesn’t have access to a variable/function, 
// we could say that variable/function is “out of scope”.

// More commonly, however, we think of a scope as a set of variables 
// and functions that are defined within that scope.