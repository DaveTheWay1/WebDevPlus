// * What are DOM Events?
// * DOM events are the bedrock of interactivity on web pages.

// * DOM events enable us as developers to implement event-driven programming.
// This programming paradigm is such that much of our code runs in response 
// to events being triggered during run-time (usually due to user interaction).

// ? Lots of events are being generated within the browser, for example, when:
// * A user moves or clicks the mouse
// * A user presses a key
// * When a form is submitted
// * When the page has finished loading or has been resized
// * etc.